//uses and displays a variable  使用并显示变量
#include <iostream>
int main(){
    using namespace std;

    int carrots;

    carrots = 25;
    cout << "I have " << carrots << "carrots" << endl;
    carrots = carrots - 1;
    cout << "Crunch, crunch, I have " << carrots << "carrots now." << endl;
    return 0;
}

